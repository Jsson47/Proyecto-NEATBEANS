    private void jBtnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIniciarActionPerformed
        // TODO add your handling code here:
        if(this.validarcampos()){
           
           String Usuario = jTxtUsuario.getText();
           String Contraseña = jTxtPassword.getText();
           
           String url = "Select Usuario, Contraseña, Privilegio from UsuariosRoles where Usuario ='"+Usuario+"'";
           
      try{
        cnx = new MS_SQLServer ();
        cnx.pst = cnx.conexion.prepareStatement(url);
        cnx.resultado = cnx.pst.executeQuery();
        
        if(cnx.resultado.next()){
            
            String User = cnx.resultado.getString("Usuario");
            String Contra = cnx.resultado.getString("Contraseña");
            String Priv = cnx.resultado.getString("Privilegio");
            
            
            if(Contraseña.equals(Contra)){
                
                if(Priv.equals("Maestrante")){
                    frmFormularios form = new frmFormularios();
                    form.jLblUser.setText(jTxtUsuario.getText());
                    form.jLblMaes.setText("Maestrante:");
                    form.jPanel4.setVisible(true);
                    form.jBtnMaestrante.setVisible(false);
                    form.jBtnPagos.setVisible(false);
                    form.jBtnCoordinador.setVisible(false);
                    form.jBtnBecas.setVisible(false);
                    form.jBtnDeuda.setVisible(false);
                    form.jBtnConfiguracion.setVisible(false);
                    form.jBtnConsolidado.setVisible(false);
                    JOptionPane.showMessageDialog(null,"Bienvenido Maestrante:"+Usuario);
                    this.dispose();
                                     
                }
                else if(Priv.equals("Coordinador")){
                   frmFormularios form = new frmFormularios();                
                    form.jLblUser.setText(jTxtUsuario.getText());
                    form.jLblMaes.setText("Coordinador:");
                    form.jPanel4.setVisible(true);
                    this.dispose();
                    JOptionPane.showMessageDialog(null,"Bienvenido Coordinador:"+Usuario);
                }
                
            }else{
                JOptionPane.showMessageDialog(null,"Contraseña Incorrecta, Revice de Nuevo");
            }
        }else{
              JOptionPane.showMessageDialog(null,"Este Usuario no esta registrado");
        }
        }catch(ClassNotFoundException ex){    JOptionPane.showMessageDialog(this, "Error al intentar guardar "
                                        + "el \n registro, no se encuentra una librería", "Librería no Encontrada", JOptionPane.ERROR_MESSAGE);
                            } catch (InstantiationException ex) {
                                JOptionPane.showMessageDialog(this, "Se ha producido una falla al "
                                        + "hacer referencia \n de una instancia",
                                        "Instancia no Encontrada", JOptionPane.ERROR_MESSAGE);
                            } catch (IllegalAccessException ex) {
                                JOptionPane.showMessageDialog(this, "Se ha denegado el acceso al "
                                        + "intentar utilizar \n la librería o instancia para guardar", "Acceso Ilegal a un Recurso", JOptionPane.ERROR_MESSAGE);
                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(this, "Se ha producido una falla con "
                                        + "el manejo de la solicitud \n en recurso de Base de Datos"
                                        + ex.getMessage(),
                                        "Error al Procesar Datos", JOptionPane.ERROR_MESSAGE);
                            }
    }   
    }//GEN-LAST:event_jBtnIniciarActionPerformed

