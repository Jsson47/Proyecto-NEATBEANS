    private void btnGeneradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneradorActionPerformed
        // TODO add your handling code here:
        char [] Mayusculas = {'A','B','C','D','E','F','G'};
        char [] minusculas = {'a','b','c','d','e','f','g'};
        char [] Numeros = {'1','2','3','4','5','6','7','8','9','0'};

        StringBuilder caracteres = new StringBuilder();

        caracteres.append(Mayusculas);
        caracteres.append(minusculas);
        caracteres.append(Numeros);

        StringBuilder contrasenia = new StringBuilder();

        for (int i = 0; i<8; i++){

            int cantidadCaracteres = caracteres.length();
            int numeroRandom = (int)(Math.random()*cantidadCaracteres);
            contrasenia.append((caracteres.toString()).charAt(numeroRandom));
        }
        this.jTxtPassword.setText(contrasenia.toString());
        JOptionPane.showMessageDialog(null, "Se ha generado una contraseña para su Usuario\n"+jTxtUsuario.getText());

    }//GEN-LAST:event_btnGeneradorActionPerformed

